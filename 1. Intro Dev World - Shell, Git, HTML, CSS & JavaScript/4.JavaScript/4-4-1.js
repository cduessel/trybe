// functions de soma, subtração, multiplicação e divisão

function soma(a, b){
    return a + b;
}
console.log(soma(5,5))

function menos(a, b){
    return a - b;
}
console.log(menos(7, 4))

function multi (a, b){
    return a * b
}

console.log(multi(3, 3))

function divi(a, b){
    return a / b;
}
console.log(divi(49, 7))

